import java.lang.*;


public class arithmeticoperator {
    public static void main(String[] args) {

        int a= 10;
        int b=3;

        // addition
        System.out.println(a+b);

        //subtraction
        System.out.println(a-b);

        //multiplication
        System.out.println(a*b);

        //division
        System.out.println(a/b);


        //Modulo
        System.out.println(a%b);

    }
}
